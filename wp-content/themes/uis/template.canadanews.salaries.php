                    <p>
                        <strong>When</strong> it comes to immigrating to a new country, a main concern is job outlook and salary. In Canada there is one thing that can't be denied - the job outlook is fantastic. Canada has a wealth of career opportunities just waiting to be taken advantage of. And being one of the most diverse countries in the world, there are equal opportunities for everyone. But how does Canada weigh in on the salary scale?
                    </p>

                    <p>
                        According to Statistics Canada, the average salary in Canada is on the rise. Since 2013, the average wage in Canada has been on a steady increase. As of 2017, the average Canadian wage was $986 per week, or approximately $51 000 per year. And according to the Organization of Economic Co-operation and Development, that means Canadians are doing pretty well for themselves in comparison to other countries. Data shows that among the 34 member states of the OECD, Canada rates number 10.
                    </p>

                    <p>
                        Of course, this statistic relates to an average income of all the jobs in Canada. But how well are the top 10 most popular professions doing? Let's take a look:

                        <ol>
                            <li>
                                <strong>Software Engineers:</strong>
                                <br/>
                                A software engineer is someone who works in the area of computer software development. They "apply principles of software engineering to the design, development, maintenance, testing, and evaluation of computer software" (Wikipedia). In other
                                words, software engineers are the reason computers can perform as many functions and operate as smoothly as they do. Most software engineers require a Bachelor’s Degree in computer science, software engineering, or mathematics, combined with a college level computer science program. In some cases, a master's or doctoral degree could be required.
                                <br/><br/>
                                And while extensive schooling may be required to become a software engineer, the payout is well worth it. The average wage of a software engineer in Canada is $72, 000 per year. In comparison with South Africa, this is much higher. The annual wage for a software engineer is South Africa is R334 148, which is equivalent to 32863.81 Canadian Dollars.
                                <br/><br/>
                                <div class="img-wrapper dark-wrapper">
                                    <img src=../../wp-content/themes/uis/inmanage/lp-pages/salaries/assets/images/image-01.jpg" alt="img description">
                                    <h3 class="img-text">Software Engineers Make in Average $72, 000 Per Year in Canada</h3>
                                </div>
                            </li>
                            <li>
                                <strong>Nurses:</strong>
                                <br/>
                                A nurse is someone who plays an integral role in the health care system, and works to promote the health and care of the physically and mentally ill, as well as the disabled. Nurses can work in a variety of different settings including hospitals, long term care homes, schools, and community health centers. The level of certification you will need to become a nurse depends on which type of nursing you wish to get into. The most common type of nursing, Registered nursing, requires a diploma, an Associate Degree, or a Bachelor of Science.
                                <br/><br/>
                                The average salary for a registered nurse in Canada is $65 000 - $85 000. Again, this is much higher than the South African rate of R211, 898 ( 20 825.92 Canadian dollars).
                                <br/><br/>
                                <div class="img-wrapper dark-wrapper">
                                    <img src=../../wp-content/themes/uis/inmanage/lp-pages/salaries/assets/images/image-02.jpg" alt="img description">
                                    <h3 class="img-text">Registered Nurses will Make Between $65,000 - $85,000 Per Year in Canada</h3>
                                </div>
                            </li>
                            <li>
                                <strong>Doctors:</strong>
                                <br/>
                                Doctors are physicians who study health and medicine. There are many different types of doctors in Canada including family physicians, pediatricians, dermatologists, surgeons, and so on and so forth. Again, the requirements to become a doctor vary by specialty. In general, however, doctors require an extensive amount of education and training including a 4 year undergraduate program, a 4 year study in medical school, and 3-7 years of residency training.
                                <br/><br/>
                                The average annual salary for a doctor in Canada is $339, 000, which puts doctors at the top of the list for the occupations with the highest incomes in Canada.  In South Africa the average income for a doctor is R472, 052 ($46 444.02).
                                <br/><br/>
                                <div class="img-wrapper dark-wrapper">
                                    <img src=../../wp-content/themes/uis/inmanage/lp-pages/salaries/assets/images/image-03.jpg" alt="img description">
                                    <h3 class="img-text">Doctors at the Top Paid List Occupations in Canada</h3>
                                </div>
                            </li>
                            <li>
                                <strong>Digital Marketing Managers:</strong>
                                <br/>
                                Though a Digital Marketing Manager does a variety of different things, their job can be summed up as someone who develops, implements, and manages marketing campaigns to help promote a company and its products or services. As with most positions in Canada, educational requirements will vary depending on the exact job description, but a Bachelor’s degree is recommended if you want to be competitive in the industry.
                                <br/><br/>
                                The average income for a Digital Marketing Manager in Canada is $62, 984. In South Africa, the average income is R142 128 ( $13 965 Canadian)
                                <br/><br/>
                                <div class="img-wrapper dark-wrapper">
                                    <img src=../../wp-content/themes/uis/inmanage/lp-pages/salaries/assets/images/image-04.jpg" alt="img description">
                                    <h3 class="img-text">Digital Marketing Managers Develop, Implement, and Manage Marketing Strategy</h3>
                                </div>
                            </li>
                            <li>
                                <strong>Teachers :</strong>
                                <br/>
                                Teachers are there to help educate and inspire others. Their job title includes preparing lesson plans and presentations, grading papers, managing the classroom, meeting with parents, and working closely with other staff. While it's not in their actual job description, most teachers also act as mentors and role models to their students. Teachers can educate in elementary schools, high schools, colleges, universities, special education programs, and even ESL programs. Basic requirements to become a teacher include a Bachelor’s degree in Education and a Provincial Teaching Certificate. If you wish to teach in French or French Immersion schools, a Bachelors of education is also required in French.
                                <br/><br/>
                                The average high school teacher in Canada earns $57, 062 per year. This is compared to South Africa where teachers earn an average of R 179 000 ($17 578 Canadian).
                                <br/><br/>
                                <div class="img-wrapper dark-wrapper">
                                    <img src=../../wp-content/themes/uis/inmanage/lp-pages/salaries/assets/images/image-05.jpg" alt="img description">
                                    <h3 class="img-text">Educating the Canadian Next Generation, Teachers in Canada</h3>
                                </div>
                            </li>
                            <li>
                                <strong>Early Childhood Educators:</strong>
                                <br/>
                                Early Childhood Educators take on much of the same roles as teachers, except they work with much younger children. Most Early Childhood Education centers work with children starting the age of 6 months. Because the children are much younger, the role of Early Childhood Educator may require more one-on-one care and nurturing than the typical teaching role. Early Childhood Educators are required to have a 2-4 year Early Childhood Education certificate from a college level program.
                                <br/><br/>
                                The average salary for an Early Childhood Educator in Canada is $33 413. In South Africa it is R 69 807 ($6 857 CAD).
                                <br/><br/>
                            </li>
                            <li>
                                <strong>Administrative Assistants:</strong>
                                <br/>
                                Administrative Assistants are individuals who help to assist in office duties such as answering telephones, setting appointments, filing paperwork, and so on and so forth. Administrative assistants are always in high demand in Canada because they are needed in almost every industry. While there are no particular requirements to become an administrative assistant in Canada, most employers do prefer candidates with some type of educational degree, as well as some relevant experience in the related industry.
                                <br/><br/>
                                The average salary for an Administrative Assistant in Canada is $39 304. In South Africa the average salary is R 84 454 ($8296 Canadian).
                                <br/><br/>
                            </li>
                            <li>
                                <strong>Carpenters:</strong>
                                <br/><br/>
                                While there are not many women in the carpentry field, it is one of the leading professions for men. Carpenters are people who construct, build, and repair wooden structures. They can hold jobs in the area of construction, inspection, drywall, flooring, roofing, insulation, and even concrete. Formal education is not required to become a carpenter, but some do opt to enroll in apprenticeship programs. On the job training is often provided.
                                <br/><br/>
                                Because many carpenters work on a job-to-job basis, it's difficult to determine an annual salary range. In addition, the pay gap is large, ranging anywhere from $16.09 - $35.13 per hour. In South Africa the average salary for a carpenter is R 10 941 ($1 075 Canadian) per month.
                                <br/><br/>
                            </li>
                            <li>
                                <strong>Automotive service technicians:</strong>
                                <br/><br/>
                                An automotive repair service technician is someone who examines, tests, and repairs the parts of cars and trucks. Also referred to as auto mechanics, this job requires a minimum of a GED certificate or high school diploma. With that being said, most employers place emphasis on those who posses an associate degree or certificate in automotive service technology.
                                <br/><br/>
                                The average income of an auto service technician in Canada is $55 972. In South Africa it is R 147, 407 ($14 507 Canadian)
                                <br/><br/>
                            </li>
                        <li>
                            <strong>Real Estate Agents</strong>
                            <br/><br/>
                            Real estate agents sell or rent out property or land for their clients. They are active in all parts of a sale including property listings, interviews, viewings, discussing conditions of sale, and drawing up contracts. Educational requirements for realtors vary by province, but most do require some type of post-secondary education in the field. A written exam must also be passed to qualify as an agent.
                            <br/><br/>
                            In Canada, the average income for a real estate agent is $106 231. In South Africa it is R 120 852 ($11 893 Canadian).
                            <br/><br/>
                            offer quite attractive salaries. Of course, these are just a few of the many opportunities that are available to immigrants coming to Canada.
                        </li>
                        </ol>
