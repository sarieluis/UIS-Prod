                    <h1>Canada’s to Allow One Million New Immigrants over the Next Three Years</h1>
                    <div class="user-information">
                        <div class="img-holder"><img src="../../wp-content/themes/uis/inmanage/lp-pages/jobsearch/assets/images//img-2.png" alt="img description"></div>
                        <div class="text-holder">
                            <p>Written By <a href="#">Leonard Standford</a> , UIS Writer.</p>
                            <p>Updated 08:03 PM ET,  <time datetime="01-02-2017">FR Jan 25th 2019</time></p>
                        </div>
                    </div>
                    <p>
                        <strong>Canada</strong> has always been an open and welcoming country, but within the next three years their generosity is to reach an all time high. As the liberal government states, Canada’s new immigration strategy is expected to demonstrate, “the most ambitious immigration levels in recent history”. Just how ambitious are the new Canadian immigration policies? Over the next three years, Canada is expecting to welcome over one million new immigrants into the country, bringing immigration up to one percent of the total population by 2020.
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/million-immigrants/assets/images/image-01.jpg" alt="img description">
                        <h3 class="img-text">Leading a liberal immigration policy, Prime Minister Justin Trudeau</h3>
                    </div>

                    <p>
                        In charge of the decision to raise immigration levels is the newly appointed Minister of Immigration, Ahmed Hussen. Hussen was appointed Minister of Immigration in 2017 as part of a cabinet shuffle by Prime Minister, Justin Trudeau. Born and raised in Somalia, Hussen immigrated to Canada in 1993 in an attempt to flee the civil war. He was just 16 years old when he settled in the Regent Park area of Toronto, striving to make a life in Canada all on his own. At the age of 22 Hussen graduated from law school, and in 2002 he went on to co-found the Regent Park Community Council, raising over $500 million for revitalization of the area. Before his time in cabinet, Hussen also served as the National President of the Canadian Somali Congress - an organization that advocates for the importance of Somali heritage.
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/million-immigrants/assets/images/image-02.jpg" alt="img description">
                        <h3 class="img-text">Ahmed Hussen, first Somali-born Canadian Minister.</h3>
                    </div>

                    <p>
                        So what should we expect to see from Hussen in the next few years? In keeping things simple, we should expect to see a drastic, yet gradual spike in immigration levels. In 2017, Canada let in over 300, 000 immigrants. This year, Hussen hopes to increase that number to 310, 000. By 2019, this number will be raised to 330, 000, and by 2020 we should expect to see over 340, 000 new immigrants in Canada. (See chart below).
                    </p>

                    <p>
                        In addition to allowing more immigrants in, the government is also working hard to reduce backlogs and speed up the application process. The goal here, of course, would be to reunite families in a more timely manner than has been completed in the past. In fact, the Trudeu government has now announced that over $113 million will be dedicated to improving “pre-arrival” services for immigrants to Canada. This money will not only go towards more specialized information and services, but will also go towards educating new immigrants on what life is like in Canada, and dealing with any issues or concerns that may arise once they are here. Hussen stated that when he arrived in Canada at the age of 16, he “relied on the generosity of Canadians” to help him through. And while he still hopes that the generosity of Canadians is seen to future immigrants, he also hopes that these new programs will help new immigrants avoid some of the early challenges of immigration that he once faced.
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/million-immigrants/assets/images/image-03.png" alt="img description">
                        <h3 class="img-text" style="padding:0;">Welcoming more Immigrants each year</h3>
                    </div>

                    <p>
                        But these new immigration policies aren’t just good news for immigrants. According to the government, they’re also good news for Canadians. Hussen states that “newcomers play a vital role in our society”, especially in the economic and workforce sectors. By 2035, over 5 million Canadians will be set to retire. This will leave huge gaps in the labor force, which can then be filled by new immigrant workers. According to the government, the vast majority of newcomers are enrolled in programs that are centered around addressing the skill shortages in the labor force.
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/million-immigrants/assets/images/image-04.jpg" alt="img description">
                        <h5 class="img-text">Expanding educational facilities to serve the new waves of immigrants.</h5>
                    </div>

                    <p>
                        And as much as the government would love to raise immigration numbers to 450, 000 per year immediately, it also recognizes that this simply isn’t possible. Hussen reminds Canadians that we need to keep in mind that newcomers need access to suitable and appropriate settlement services like housing and education, and this requires both time and money. In his own words, Hussen states, “it’s a question of gradual increase so our immigration system can be able to process these things”.
                    </p>

                    <p>
                        Nevertheless, Hussen recognizes that there needs to be more done to aide refugees around the world and will continue to fight to raise immigration numbers every single day. As such, there’s no better time to immigrate to Canada than now, after the Canadian government has decided to accept “the most ambitious” immigration rates in Canadian history.
                    </p>

