                    <h1>The Costs of Living in Canada</h1>
                    <div class="user-information">
                        <div class="img-holder"><img src="../../wp-content/themes/uis/inmanage/lp-pages/costofliving/assets/images//img-2.png" alt="img description"></div>
                        <div class="text-holder">
