                    <p>
                        <strong>Beautiful</strong> scenery, equal opportunities, universal health care - there are many reasons that one might choose Canada as the perfect immigration location. But before you take the plunge, it’s important that you figure out how you will support yourself once you get there. And in determining whether or not you can afford to do so requires some research into Canada’s living costs. So today we will compare the living costs of Canada to other parts of the world, and give you a short breakdown on some of the day-to-day living costs that you might encounter.
                    </p>


                    <p>
                        Here’s the bad news - in comparison to some other countries around the world, the cost of living in Canada is pretty high. How high? According to a survey by Deutsche Bank, Canada is the 5th most expensive country in the world to live in. It falls behind Australia, New Zealand, Britain, and the US. The good news is, that Canadians are also earning a significant amount more than other countries. On a list of wages from 35 different countries around the world, Canada ranks number 11, with an average annual income of $48 403. So while the cost of living in Canada may be higher than other countries, so is the average income. It really balances itself out.
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/costofliving/assets/images/image-01.jpg" alt="img description">
                        <h3 class="img-text">Making and Paying More, Toronto Downtown</h3>
                    </div>

                    <p>
                        Another thing that’s important to know about Canada is that there is a vast difference in the cost of living between rural areas and major cities. Life in major cities, such as Toronto and Vancouver, will cost much more than life in a small rural town. With that being said, most immigrants that do come to Canada start in the bigger cities like Toronto and Vancouver. As such, we’re going to focus much of our data on the cost of living in areas such as these. So what should you expect to pay to live in a big city in Canada? Let’s take a look at some averages:
                    </p>

                    <p>
                        Average monthly rent for a 900 square foot apartment : $2, 369 <br/>
                        Average monthly rent for a 3 bedroom apartment : $3 209.71 <br/>
                        Average monthly utility costs in a 1 bedroom apartment  : $105 <br/>
                        Average price of a house : $772, 400 <br/>
                        Average cost for a new vehicle : $33 464 <br/>
                        Average price of gas: 136 cents per litre. <br/>
                        Average price of a meal at an inexpensive restaurant: $16.50<br/>
                        Average cost of a bus ticket: $3.25 Average cost of childcare: $1 343.75 per month<br/>
                        Average cost of a pair of jeans : $66.52 <br/>
                        Average cost of a getaway cottage: $450, 000 <br/>
                        Average cost of a dirt bike: $8849
                    </p>


                    <p>
                        These are just a few of the average costs to give you an idea of what the basic necessities, as well as some of the luxuries cost living in a large city in Canada. Keep in mind that these are averages for Canada’s larger cities. If you choose to live in a more rural area, prices are almost guaranteed to be lower.
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/costofliving/assets/images/image-02.jpg" alt="img description">
                        <h3 class="img-text">Suburbs Property will Cost Less than Urban One</h3>
                    </div>

                    <p>
                        Despite the fact that Canada ranks higher on the cost of living scale, it’s important to remember that there are a lot of areas in Canada where you save money as well. In Canada, everyone is entitled to a free public education until they are 18 years of age. This means that all elementary and secondary schooling is completely free. Canada also offers free health insurance. Unlike the U.S. where people have to pay monthly rates for their health insurance, all Canadians who have been accepted for public health insurance receive free health care. Of course, this doesn’t mean that absolutely everything is paid for, but what isn’t covered by the government is often covered as benefits by full-time employers. You can also opt for private health insurance to cover things that public health insurance does not (prescription medications, dental costs, ambulatory services, prescription glasses, etc).
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/costofliving/assets/images/image-03.jpg" alt="img description">
                        <h3 class="img-text">Great Value for Your Money, Vancouver Restaurant</h3>
                    </div>

                    <p>
                        So yes, the costs of living in Canada are higher than they are other places in the world, but the benefits of living here highly outweigh the costs. Free healthcare, free education, equal employment opportunities, and the freedom to live and do as you please are just a few of the many things that makes Canada one of the best places in the world to live.
                    </p>

