                    <h1>How to Look for a Job in Canada</h1>
                    <div class="user-information">
                        <div class="img-holder"><img src="../../wp-content/themes/uis/inmanage/lp-pages/jobsearch/assets/images//img-2.png" alt="img description"></div>
                        <div class="text-holder">
                            <p>Written By <a href="#">Leonard Standford</a> , UIS Writer.</p>
                            <p>Updated 06:53 PM ET,  <time datetime="01-02-2017">TU August 21th 2018</time></p>
                        </div>
                    </div>
                    <p>
                        <strong>When</strong> immigrating to Canada, finding a job is usually on the top of the priority list. But searching for a job in a new country can be intimidating - especially if you aren't familiar with the process. Luckily, there are plenty of people and services you can turn to for help when integrating into the Canadian workforce. But before we talk about these services, why don't we first start by discussing a little about the job application process:
                    </p>

                    <p>
                       <strong>Step 1: Find a Job</strong>
                        <br/><br/>
                        When it comes to searching for a job in Canada, there are many services that you can turn to. The most common form of job search in Canada is via the internet. Canada has several different websites where employers can post job offerings, and job searchers can apply directly through the website. Common websites used for job search in Canada include:
                        <ul>
                        <li>Indeed</li>
                        <li>CharityVillage</li>
                        <li>Monster</li>
                        <li>Simply Hired</li>
                        <li>Workopolis</li>
                        <li>Government Job Bank</li>
                    </ul>
                        Other methods of finding jobs in Canada include:
                        <ul>
                            <li>Directly contacting employers</li>
                            <li>Attending job fairs in your city or town</li>
                            <li>Looking in the newspaper classified sections</li>
                            <li>Visiting an unemployment agency</li>
                        </ul>

                    </p>
                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/jobsearch/assets/images/image-01.jpg" alt="img description">
                        <h3 class="img-text">Immigrant's First Step is Finding a Job</h3>
                    </div>

                    <p>
                       <strong>Step 2: Create a Resume and Cover Letter</strong>
                        <br/><br/>
                        Once you have found a job that you would like to apply to, you will need to provide the employer with your resume and cover letter.
                        <br/><br/>
                        <strong>Resume:</strong> A resume is short document that summarizes your education, work experience, and skills. A resume is required anytime you apply to a new position. A good resume will include:
                        <ul>
                        <li>
                            Your personal details including your full name and contact information
                        </li>
                        <li>
                            Your career objective (what you wish to accomplish though the career)
                        </li>
                        <li>
                            Your educational details
                        </li>
                        <li>
                            Your previous related work expereince
                        </li>
                        <li>
                            Any skills or strengths that would make you a good candidate for the position
                        </li>
                        <li>
                            References - though references are not always required, providing an employee with three references is always recommended. Whenever possible, these should be work related references.
                        </li>
                    </ul>
                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/jobsearch/assets/images/image-02.jpg" alt="img description">
                        <h3 class="img-text">Creating Resume and Cover Letter </h3>
                    </div>
                    <br/><br/>
                    <strong>Cover letter: </strong>A cover letter is a single page letter that is written to the employer. A cover letter should provide the employer with an introduction to who you are and how your skills or experience would be an asset to the position at hand. Though cover letters are not always requested by employers, they are always recommended. Each cover letter should be unique and directed towards the company of which you are applying. It should include :
                    <ul>
                        <li>An introduction to yourself </li>
                        <li>The name of the position you are applying for</li>
                        <li>A brief description of how your skills and experience would be an asset to the position</li>
                        <li>A call to action (request to meet, request to have an interview, etc).</li>
                    </ul>
                    </p>

                    <p>
                        <strong>Step 3: Wait for a Call back </strong>
                        <br/><br/>
                        If an employer finds that you might be a good fit for the position, they will respond to you via your method of contact listed on your resume. Depending on how urgently they are looking to employ, call backs can take anywhere from a few days to a few weeks. During a call back, the employer will request to bring you in for an interview. You can set a time that works best for both of you, although it is strongly recommended that you do your best to work with the employers time schedule.
                    </p>

                    <p>
                        <strong>Step 4: The Interview</strong>
                        <br/><br/>
                        An interview is a good way for you and your potential employer to get to know each other. This is a time where you can learn more about the job, and ask any questions that you may have. It also gives the employer a chance to ask you questions to help determine whether you would be a good fit for the position. While the main piece of advice for going into an interview is to showcase your personality and be yourself, there are some typical Canadian etiquette rules that you should follow:
                        <ul>
                        <li>Turn your cellphone off</li>
                        <li>Dress professionally </li>
                        <li>Look your interviewer in the eyes when speaking to them</li>
                        <li>Let the company take the lead</li>
                        <li>Sit up straight</li>
                        <li>Arrive 15 minutes early</li>
                        <li>Send a thank you note once the interview is finished</li>
                    </ul>
                    </p>
                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/jobsearch/assets/images/image-03.jpg" alt="img description">
                        <h3 class="img-text">Good Way to Know Each Other, Interview</h3>
                    </div>

                    <p>
                        <strong>Step 5: Getting the job</strong>
                        <br/><br/>
                        If the employer thinks you will be a good fit, they will call you to let you know that you got the job. When this happens, they will set up your start date and give you further instructions on what to do when you arrive. But what do you need to know about the Canadian workforce?
                        <ul>
                            <li>You have the right to equal employment</li>
                            <li>Even though you are an immigrant, you still have the right to equal employment in Canada. This means that no employer has the right to turn you down based on your race, religion, sex, age, or beliefs. Under the Employment Equity Act, federal law states that all “federally regulated organizations and businesses are to provide equal employment opportunities to four designated groups: women, Aboriginal people, people with disabilities, and members of visible minorities.”</li>
                            <li>You have the right to a safe and healthy workplace. This means that you have the right to refuse unsafe work, including situations where workplace violence may be a concern. </li>
                            <li>You have the right to be trained to deal with any hazards in the workplace </li>
                            <li>You have the right to be paid the same amount as homegrown Canadian citizens</li>
                            <li>You have the right to join a Union</li>
                        </ul>
                    What is a union? While not all do, many workplaces in Canada have unions. When they do, you have the right to join. Unions are a group of people that stand up for fair wages, safe workplaces, and fair working hours. They will help to defend your wages, your vacation time, your benefit plans, and so on and so forth. Being part of a union can help to protect all of the rights listed above.
                    <br/><br/>
                    <strong>Getting Help:</strong>
                    <br/><br/>
                    The Canadian Government offers many resources for immigrants who are looking to transition into the Canadian workforce. There are plenty of resources available where you can learn more about the Canadian workforce, get assistance in writing a resume and cover letter, and find help in the job search process. Take a look around your community to find where resources are available to you.

                    </p>

