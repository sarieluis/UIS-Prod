                            <p>Written By <a href="#">Joseph Maron</a> , UIS Writer.</p>
                            <p>Updated 08:25 PM ET,  <time datetime="01-02-2017">WE August 15th 2018</time></p>
                        </div>
                    </div>
                    <p>
                        <strong>There</strong> are a million good reasons to immigrate to Canada. Not only is it one of the best places in the world to live, but it’s also one of the most culturally diverse countries in the world. In addition, Canada has low crime rates, attractive job opportunities, and an excellent health care system. But on top of all that, the Canadian Government treats new immigrants very well, offering them a variety of rights and services upon their new residency in Canada. Today we’ll look at some of these benefits that increase the attraction to Canada all the more:
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/bookofrights/assets/images/image-04.jpg" alt="img description">
                        <h3 class="img-text">Generous Immigration Policy Package, Canada</h3>
                    </div>

                    <p>
                        <ol>
                        <li>
                            <strong>Tax Benefits and Exemptions</strong>
                            <br/>
                            Every Canadian resident must file tax returns every year - this is true even if they have a balance of $1.00 in their account. As a new immigrant, you must file for taxes after your first year of residency in Canada. With that being said, new immigrants are offered attractive tax benefits and exemptions by the government. As a new immigrant to Canada, if you fit certain criteria, you may be able to apply for some tax credits, benefits, and exemptions. Included within these are the:
                            <br/>
                            <br/>
                            <ul>
                                <li>Canadian Child Benefit (for couples or singles with children)</li>
                                <li>GST/HST Credit </li>
                                <li>Provincial and Territorial benefits and credits</li>
                            </ul><br/>
                            To learn more about how to file these credits, visit the Government of Canada <a href="https://www.canada.ca/en/revenue-agency/services/tax/international-non-residents/individuals-leaving-entering-canada-non-residents/newcomers-canada-immigrants.html#nwc" target="_blank">Website</a>.
                            <br/><br/>
                            <div class="img-wrapper dark-wrapper">
                                <img src=../../wp-content/themes/uis/inmanage/lp-pages/bookofrights/assets/images/image-01.jpg" alt="img description">
                                <h3 class="img-text">Whale Humpback Mammal Baybulls Newfoundland</h3>
                            </div>
                        </li>

                        <li>
                            <strong>LINC classes and ESL (English as a Second Language) Programs</strong>
                            <br/>
                            LINC, short for “Instruction for Newcomers to Canada”, is a government funded program that helps to ease the process of immigration to Canada. LINC is free for immigrants, and provides them with the basic language skills that they need to succeed here. In most provinces LINC programs are offered in English, but some provinces may also offer French options. Full and part time class options are available, and many centers for learning will also offer free child-care during classes.
                            <br/><br/>
                            <iframe id="YouTubeVideo" width="560" height="315" src="https://www.youtube.com/embed/ZWNtGGrkJRg" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe>
                            <br/><br/>
                        </li>

                        <li>
                            <strong>Community Support Services </strong>
                            <br/>
                            When you immigrate to Canada, you don’t have to do it alone. Within every community you will find support services nearby. The actual service provided to you may vary depending on your location, but support services are set up around Canada to help ease the immigration process, and make your transition to Canada an easier one. Community support programs are set in place to help new immigrants:
                            <br/>
                            <br/>
                            <ul>
                                <li>Find permanent housing</li>
                                <li>Find employment</li>
                                <li>Enroll your children in school or child-care</li>
                                <li>Access programs related to education, language, and skills training</li>
                                <li>Gain access to information regarding healthcare and social services</li>
                                <li>Find translators or interpreters</li>
                            </ul><br/>
                            In other words, community support services can help to answer any questions or concerns that you may have when immigrating to Canada, and are always there to help you propel forward towards a smoother transition.
                            <br/><br/>
                            <div class="img-wrapper dark-wrapper">
                                <img src=../../wp-content/themes/uis/inmanage/lp-pages/bookofrights/assets/images/image-03.jpg" alt="img description">
                                <h3 class="img-text">Helping Immigrants with Housing, Employment, School Enrollment and More</h3>
                            </div>
                        </li>

                        <li>
                            <strong>Financial Assistance for Refugees</strong>
                            <br/>
                            Moving to a new country can be intimidating. On top of packing up and leaving everything you have ever known, now you also have the worry of how you will travel, where you will live, and how you will get your basic necessities. Luckily, the Canadian Government provides plenty of assistance for Canadian first timers. Not only does the Government provide transportation upon your arrival to Canada, but they also provide you with a temporary place to live, as well as assistance in finding a more permanent residence. The Canadian government will also provide all refugees with the basic household items and necessities that they need until they are more permanently settled.
                            <br/><br/>
                            Of course, these are just a few of the many incentives offered by the government for immigrants to Canada. The government also provides access to what is known as “pre-arrival services”. These are free online or in-person programs that people can take before they arrive in Canada. They may include information regarding your profession, your destination, language training, or skills training. You can access a complete list of pre-arrival <a href="https://settlement.org/ontario/immigration-citizenship/immigrating-to-ontario/general-information-about-applying/what-are-pre-arrival-services/" target="_blank">services</a>.
                            <br/><br/>
                            Please not that refugees may qualify for even further support from the government including trauma counselling services, life skills training, personal finance assistance, and monthly income support.
                        </li>

                    </ol>
                    </p>

