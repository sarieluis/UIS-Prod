                    <h1>Everything an Immigrant needs to know about the Canadian Educational System</h1>
                    <div class="user-information">
                        <div class="img-holder"><img src="../../wp-content/themes/uis/inmanage/lp-pages/educationalguide/assets/images//img-2.png" alt="img description"></div>
                        <div class="text-holder">
                            <p>Written By <a href="#">Edwardo Perez</a> , UIS Writer.</p>
                            <p>Updated 9:56 AM ET,  <time datetime="01-02-2017">TU July 17th 2018</time></p>
                        </div>
                    </div>
                    <p>
                        <strong>Did</strong> you know that over 30% of Canada's school children are either immigrants themselves or have at least one parent that was born abroad? That's a highly significant number! So when people immigrate to Canada, it's important that both parents and children feel comfortable with the educational system. And the first step to feeling comfortable? Understanding. So today we will discuss everything an immigrant needs to know about the Educational system in Canada.
                    </p>

                    <div class="img-wrapper dark-wrapper"><img src=../../wp-content/themes/uis/inmanage/lp-pages/educationalguide/assets/images/Library_Canada_Best.jpg" alt="img description">
                        <h3 class="img-text">High School Library in Toronto.</h3>
                    </div>

