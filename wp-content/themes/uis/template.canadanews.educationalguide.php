                    <p>
                    <ul>
                        <li>
                            <strong>In Canada there are 3 different levels to the educational system: </strong>
                            <ul>
                                <li>
                                    Primary Education - Sometimes referred to as "elementary school", primary education runs from Kindergarten through Grade 8. Children generally start kindergarten around 5. Those who start earlier might go to junior kindergarten, and those who start later may start in senior kindergarten. Most children are 13 or 14 when they finish grade 8 and enter into the next stage of the educational system, secondary school.
                                </li>

                                <li>
                                    Secondary Education - Secondary education is also referred to as "high school". It runs from grade 9-12. Ontario has an additional year of secondary schooling that is optional, known as 12+.
                                </li>

                                <li>
                                    Post-secondary Education - This is where you go to school to learn more about your job field of interest. There are many colleges and universities throughout Canada that offer great programs in variety of different areas.
                                </li>
                            </ul>
                        </li>
                        <li>
                            <strong>Each classroom follows a curriculum</strong> - In Canada, each grade level has a designated curriculum that teachers must follow. The curriculum refers to a designated set of lessons or concepts that each student must learn before moving onto the next grade.
                            </br></br>
                            <div class="img-wrapper dark-wrapper"><img src=../../wp-content/themes/uis/inmanage/lp-pages/educationalguide/assets/images/Classroom.jpg" alt="img description">
                                <h3 class="img-text">Classroom: Elementary School Classroom in Vancouver.</h3>
                            </div>
                        </li>

                        <li>
                            <strong>Teachers in Canada are required to have two things:</strong> A Bachelors Degree in Education and A Provincial Teachers Certificate. It is also helpful a Bachelors of Education in French.
                        </li>

                        <li>
                            <strong>Canada has mixed classrooms</strong> - In Canada, boys and girls learn in the same classroom. For those who prefer otherwise, some private schools offer all-boy or all-girl options.
                        </li>

                        <li>
                            <strong>Canada offers  an English as a second language program</strong> - English as a Second Language Programs, more commonly referred to as ESL programs, are focused on helping students who do not speak English as their primary language. These courses often include a combination of speaking, listening, reading, and writing activities that are designed to immerse immigrants into the English language.
                            </br></br>
                            <div class="img-wrapper dark-wrapper"><img src=../../wp-content/themes/uis/inmanage/lp-pages/educationalguide/assets/images/English_Second_Language.jpg" alt="img description">
                                <h3 class="img-text">English as a second language Program Student.</h3>
                            </div>
                        </li>

                        <li>
                            <strong>Canada offers a French Immersion </strong> - French is Canada's second language. As a result, you can choose to study in French or English. French Immersion programs are designed for those who do not speak French as a primary language, but who would like to receive all teaching instructions in French as a way to learn.
                        </li>

                        <li>
                            <strong>Canada offers religious options </strong> - The Canadian educational system is open to those of any religion. With that being said, some provinces do offer separate Catholic Schools for those who wish to practice their religion at school.  Many communities also offer private schools that are adhere to different religious beliefs. Keep in mind, however, that private schools are not free to attend.
                        </li>

                        <li>
                            <strong>Canada offers Special Needs Programs</strong> - These programs are designed to give specialized attention to helping those with different types of special needs. These programs are offered to those with physical, cognitive, psychological, emotional, behavioral, and linguistic concerns. These programs are designed to take an inclusive approach to children with special needs and disabilities so that they have an equal opportunity to learn and socialize with other children. By law, all state schools in Canada must offer Special Needs Programs.
                            </br></br>
                            <div class="img-wrapper dark-wrapper"><img src=../../wp-content/themes/uis/inmanage/lp-pages/educationalguide/assets/images/Canada_Education.jpg" alt="img description">
                                <h3 class="img-text">School Bus in Alberta, Canada.</h3>
                            </div>
                        </li>

                        <li>
                            <strong>There are 4 different ways to school your children in Canada:</strong>
                            <ul>
                                <li>Free Public Schools</li>
                                <li>Paid for Private schools</li>
                                <li>At-home education</li>
                                <li>French Immersion programs</li>
                            </ul>
                        </li>

                        <li>
                            <strong>School year</strong> - The school years usually begins in September and finishes around the end of June. Children go to school Monday to Friday, and then have weekends off.
                        </li>

                        <li>
                            <strong>In Canada, your postal code does not matter</strong> - You can enroll your child in any school that you like, regardless of where you live.
                            </br></br>
                            <div class="img-wrapper dark-wrapper"><img src=../../wp-content/themes/uis/inmanage/lp-pages/educationalguide/assets/images/Edmonton_School.jpg" alt="img description">
                                <h3 class="img-text">Edmonton School Facility.</h3>
                            </div>
                        </li>

                        <li>
                            <strong>In Canada, report cards are given out several times during the school year</strong> - These are designed to give parents an insight into their child's progress in school throughout the year.
                        </li>

                        <li>
                            <strong>Canada sometimes experiences extreme weather conditions</strong> - On days that weather is deemed unsafe to attend school due to snowstorms or severe cold, you can hear about school closings by calling the school, on the radio, or on the local news.
                        </li>

                        <li>
                            <strong>In Canada most public schools do not have a dress code</strong> - With that being said, many Catholic and private schools do have dress codes that need to be adhered to.
                        </li>

                        <li>
                            <strong>Canada offers a variety of services that help immigrants integrate into the educational system:</strong>
                            <ol>
                                <li>Settlement Workers in Schools (SWIS) - These are people who are available to help newcomer families and students when they first move to Canada. They are available to help both the family and the child adjust to the Canadian educational system, and deal with any problems they might encounter along the way. SWIS workers can provide both assistance and information in one-on-one sessions, as well as in groups.</li>
                                <li>Reception or Assessment Centers - These are offered by some schools to help newcomer families learn more about school and help them to register their children. Here you can learn about different educational concepts and courses offered.</li>
                            </ol>
                        </li>

                        <li>
                            <strong>In Canada, your child is required to be immunized against specific diseases.</strong> Your doctor will provide you with a list of your child's immunizations, which you will take to the school to provide them with up to date records.
                        </li>

                        <li>
                            <strong>In order to register your child for school you will need: </strong>
                            <ul>
                                <li>Proof of child's age in the form of a birth certificate or passport</li>
                                <li>Proof of address in the form of bank statement, bill, or lease</li>
                                <li>Proof of Guardianship if children under 18 are not living with parent </li>
                                <li>Immunization records</li>
                            </ul>
                        </li>
                    </ul>

                    <p>
                        Keep in mind that even if you do not have all of the above documents, all children aged 6-18 have the right to a publicly funded education in Canada. When it comes to immigration, children may attend school regardless of their families access to documentation. Children may not be denied the right to education based on their, or their parents immigration status. Canada seeks to protect the rights of all individuals living in and coming to the country.  One way they do this is by offering publicly funded education to everyone.
                    </p>

