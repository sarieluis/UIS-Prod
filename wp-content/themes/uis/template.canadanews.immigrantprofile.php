                    <p>
                        <strong>Canada</strong> has an extremely generous immigration policy, welcoming approximately 250, 000 new immigrants every year. With that being said, there are some regulations that the government sets to determine who is offered priority or express entry. In most cases, priority is put on individuals who have “the skills and experience required to meet Canada’s economic needs”.
                    </p>

                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/immigrantprofile/assets/images/image-04.jpg" alt="img description">
                        <h3 class="img-text">Hand Picking Candidates, Canadian Governement</h3>
                    </div>

                    <p>
                        <strong>How does Canada’s Visa and Immigration Policy work?</strong>
                        <br/>
                        There are two different categories of immigrants in Canada:
                        <ul>

                          <li>
                              Those applying for permanent residency visa
                          </li>

                        <li>
                            Those applying for temporary residency visa, work permit, or working visa
                        </li>

                        </ul>
                    Those applying for permanent residency are divided into several other different categories:
                    <ul>

                        <li>
                            Economic class (investors, entrepreneurs, self-employed persons, etc)
                        </li>
                        <li>
                            Family class
                        </li>
                        <li>
                            Refugees
                        </li>
                    </ul>
                    Who receives priority depends on which category they fall into.</p>
                    <div class="img-wrapper dark-wrapper">
                        <img src=../../wp-content/themes/uis/inmanage/lp-pages/immigrantprofile/assets/images/image-01.jpg" alt="img description">
                        <h3 class="img-text">The Canadian Parliment has passed an extremely generous immigration policy, welcoming approximately 250, 000 new immigrants every year</h3>
                    </div>


                    <p>
                        For those applying for permanent residency, entrance into Canada works on a Points-Based-System. This system helps to narrow down applicants who show an ability to become economically established within Canada. If you obtain enough points, you may be eligible for express entry into Canada.  Here are some things the Canadian Government considers when narrowing down the economic class of immigration applicants:

                    <ol>
                        <li>
                            <strong>Language skills</strong> - Points here are given based on an individual’s ability to write, read, listen, and speak English. The better able you are to speak the language, the more points you will get. Points can be increased if you are also able to communicate in French. An approved language test is given to test an individual’s language skills. You can receive up to 25 points for your language skills.
                        </li>

                        <li>
                            <strong>Education</strong> - You can also earn points for your educational level. In order to earn these points, you must be able to prove that a) you have earned a Canadian diploma or certificate, or b) you have a foreign education that is equivelant to that of a completed credential in Canada. The higher your educational level, the more points you will receive. Those with a one-year-post-secondary program degree, for example, are eligible for 15 points, while those with a doctoral degree are eligible for 30 points. You can receive up to 24 points for your educational background.
                        </li>

                        <li>
                            <strong>Experience level</strong> - These points are based on your level of experience in certain work-related fields. The more time you have spent in an approved full-time paid occupation, the more points you will receive. You can receive up to 21 points for your related levels of experience.
                        </li>

                        <li>
                            <strong>Age</strong> - Those who are between the ages of 21 and 49 receive priority over other age categories. Anyone within this age range receives 10 points. Anyone outside of this range is deducted two points for each year that they over or under.
                        </li>

                        <li>
                            <strong>Pre-arranged employment</strong> - If an individual has been offered a permanent job within Canada, and that job has been approved by Human Resources and Skills Development Canada, then they receive an additional 10 points.
                            <br/><br/>
                            <div class="img-wrapper dark-wrapper">
                                <img src=../../wp-content/themes/uis/inmanage/lp-pages/immigrantprofile/assets/images/image-02.jpg" alt="img description">
                                <h3 class="img-text">Pre-Arranged Employment will Grant Candidates with Additional 10 Points</h3>
                            </div>
                        </li>

                        <li>
                            <strong>Adaptability</strong> - This is the category where all other factors come into play. It takes into consideration your ability to adapt into Canadian culture and environment. It may include things like your spouse’s educational background or any family relations you have in Canada. You can be awarded up to 10 points in the Adaptability category.
                        </li>
                    </ol>
                    Once points in all categories have been added together, those with the most points will be awarded priority to immigrate to Canada.
                    </p>

                    <p>
                        Having said all of this, this is only the case if you are applying for Economic class residency into Canada. There are a whole different set of laws and regulations for those applying for a work permit or working visa, as well as those applying for Family Class residency.  When applying for family class residency, there is no points-based system. Rather, preference is given to those based on their relationship to their sponsor. This means that individuals with a permanent Canadian residence can sponsor spouses or partners, children, parents, grandparents, and other relatives to immigrate to Canada. Spouses, children, parents, and grandparents generally receive priority, but other relatives may be eligible to immigrate to Canada under special circumstances.
                    </p>

                    <p>
                        It is important to know that there is no income required to sponsor your spouse or child to come to Canada. Having said that, there are stricter guidelines against sponsoring other relatives, and the sponsor must agree to give financial support to the relative for up to 10 years.
                    </p>

